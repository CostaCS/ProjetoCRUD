        <h1>KickBoxing Ichigeki</h1>
        <p>O Kickboxing surgiu na década de 70, nos Estados Unidos, quando os karatecas tradicionais começaram a adaptar as competições de forma mais profissional, com a utilização de protetores de pés e mãos para que os contatos fossem mais contundentes. A modalidade evoluiu e passou a se chamar Full Contact e posteriormente, com seu crescimento, mudado para Kickboxing.</p>

 

 <p>A WAKO (Word Association of Kick Boxing Organization), fundada em 1971, é a principal organização mundial que representa o kickboxing e está sediada em mais de 90 países.</p>

 

 <p>No início dos anos 90, surgiu no Japão o que seria o principal evento de lutas durante décadas: o K-1. O evento reunia os melhores lutadores do mundo, dos mais variados estilos (como o Muay Thai, Kickboxing, Karate, Kung-fu, Savate entre outros), numa competição realizada em formato de torneio (Grand Prix), com lutas épicas, criando lendas e gerando novas revelações.</p>

 <p>Vários brasileiros de destacaram no K-1, entre eles os atletas do Kyokushin Francisco Filho, Glaube Feitosa e Ewerton Teixeira, os quais tornaram-se referências no mundo da luta.</p>

 

 <p>Seguindo os mesmos passos do campeão Francisco Filho, o atleta de Kyokushin Marcos Costa (faixa preta 3º dan na época) iniciou seus treinamentos no kickboxing para participar em competições internacionais representando o Kyokushin.</p>

 <p>Nesta época, aproveitando o crescimento do esporte, a organização Kyokushinkaikan, em conjunto com o shihan Francisco Filho, idealizaram a criação de um evento de kickboxing onde pudessem colocar os atletas de renome do Kyokushin no mundo profissional das lutas. Assim surgia o evento Ichigeki, um torneio de lutas que segue as regras originais do K-1 e com o mesmo prestigio internacional.</p>

 

 <p>Após ter parado de competir em 2002, sensei Marcos Costa começa a dar aulas de kickboxing usando as técnicas aprendidas com o campeão Francisco Filho, com alguns mestres de muay thai e do próprio Kyokushin.</p>

 <p>Em 2003, Marcos Costa se encontra com shihan Francisco Filho e juntos começaram a desenvolver uma metodologia de treinamento de kickboxing que agregava a filosofia do Kyokushin. Assim nascia o Ichigeki kickboxing.</p>

 

 <p>Em 2015 o Ichigeki foi oficializado no Japão e agora todos os países que possuem academias de Kyokushin poderão contar com aulas de Ichigeki kickboxing.</p>
