<h6>Full Contact Karate</h6>

<p>O estilo de Karate Kyokushin foi fundado pelo mestre sosai Masutatsu Oyama (10º grau) e é considerada uma das mais contundentes modalidades de artes marciais da atualidade e uma das mais praticadas em todo o mundo.</p>

 

<p>O Kyokushin se firmou no Brasil com a chegada do shihan Seiji Isobe, no ano de 1972. Um dos melhores técnicos do estilo Kyokushin do mundo, shihan  Isobe montou sua primeira academia em Santo André, no ano de 1974, e estendendo seu trabalho em toda a América do Sul. Com a morte do Mestre Oyama, a direção geral da Organização Internacional de Karate Kyokushinkai (IKO), ficou sob a responsabilidade do mestre Shokei Matsui, que nomeou shihan Isobe para Sub-Chefe do departamento internacional da Organização.</p>

 

<p>O estilo Kyokushin, conhecido pelos rigorosos treinamentos de resistência e força, foca também a importância do lado técnico, do equilíbrio do corpo e da mente, e principalmente o respeito entre os praticantes e a espiritualidade de sua filosofia.</p>

 

<p>Com relação à parte prática, o Karate Kyokushin utiliza técnicas compactas de velocidade, combinação de golpes (chutes e socos) de impacto, visando o contato corporal e tendo como principal objetivo nas competições, o nocaute (seguindo sempre as regras de competição criadas pela Organização Internacional).</p>

<p>Apesar de sua rigidez vista nas competições, o Karate kyokushin apresenta também sua face harmônica e de equilíbrio, presente nas diversas técnicas de Katas e Idogueiko.</p>

 

<p>Atualmente o Brasil é considerado um dos países de maior força do Kyokushin, conseguindo diversos títulos importantes como os mundiais absoluto com o shihan Francisco Filho (1999) e Ewerton teixeira (2007), dois títulos mundiais peso pesado com Francisco Filho (1997) e Ewerton Teixeira (2005), do título mundial peso médio com Andrews Nakahara (2005) entre diversos outros torneios.</p>

 

<p>Até mesmo no mundo das lutas profissionais, como em grandes eventos de Kick Boxing (K-1) e MMA (Dream), grandes nomes do Kyokushin brasileiro se destacam, dentre eles, o próprio shihan Francisco Filho, e os atletas Glaube Feitosa, Marcos Costa, Ewerton Teixeira, Fabiano Silva, Andrews Nakahara, entre outros.</p>

 

<p>Outras personalidades no cenário internacional do MMA também se destacam pelas suas raízes no karate Kyokushin, entre eles os atletas Georges St Pierre (ex-campeão meio-medio UFC), Bas Rutten (ex-campeão pesado UFC), Gerard Gordeau (vice-campeão do 1º UFC), Uriah Hall (UFC), John Makdessi (UFC), Katsunori Kikuno (UFC), Semmy Schilt (UFC, K-1 e Pride) Jérôme Le Banner (K-1 e Pride) entre outros.</p>


